//Use the while loop to print n9mbers from 1 to 25 in ascending and descending order.
// Ascending Order
let a = 1;
console.log("Ascending Order:");
while (a<= 25) {
  console.log(a);
  a++;
}

// Descending Order
let b = 25;
console.log("\nDescending Order:");
while (b>= 1) {
  console.log(b);
  b--;
}
